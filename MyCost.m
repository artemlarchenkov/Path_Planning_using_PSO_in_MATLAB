function [z, sol] = MyCost(sol1, model)
    % MYCOST Вычисляет стоимость (целевую функцию) для задачи планирования пути
    
    % Преобразование представления решения
    sol = ParseSolution(sol1, model);
    
    % Веса для целевой функции и штрафа за нарушение ограничений
    beta = 100;
    
    % Расчет общей стоимости как суммы длины пути и штрафа за нарушение
    z = sol.L * (1 + beta * sol.Violation);
end
